    private void RequisitionsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RequisitionsActionPerformed
    File inventoryFile = new File(FILE_Item_Inventory);
    File requisitionsFile = new File(FILE_Requisitions);

    StringBuilder requisitionsData = new StringBuilder();
    boolean lowStockFound = false;

    try (BufferedReader br = new BufferedReader(new FileReader(inventoryFile))) {
        String line;
        while ((line = br.readLine()) != null) {
            try {
                // Assuming format: Date,Item,Quantity Sold,Price per Unit,Total Sales,Initial Stock,Quantity Remain
                String[] data = line.split(",");
                if (data.length != 7) {
                    throw new IllegalArgumentException("Invalid data format.");
                }

                String item = data[1].trim();
                int quantityRemain = Integer.parseInt(data[6].trim());

                // Check if the remaining quantity is below 20
                if (quantityRemain < 20) {
                    lowStockFound = true;
                    requisitionsData.append("Item: ").append(item)
                                    .append(" | Quantity Remain: ").append(quantityRemain)
                                    .append("\n");
                }
            } catch (Exception e) {
                // Log invalid lines (optional)
                System.err.println("Skipped invalid line: " + line);
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error reading inventory data: " + e.getMessage());
        return;
    }

    // Write low stock items to Requisitions.txt
    if (lowStockFound) {
        try (BufferedWriter bw = new BufferedWriter(new FileWriter(requisitionsFile))) {
            bw.write(requisitionsData.toString());
            JOptionPane.showMessageDialog(this, 
                "Requisitions have been successfully recorded in Requisitions.txt.\n\nLow Stock Items:\n" 
                + requisitionsData.toString(), 
                "Requisitions", JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error writing requisitions data: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "All items have sufficient stock. No requisitions needed.", 
                                      "Requisitions", JOptionPane.INFORMATION_MESSAGE);
    }
    
    }//GEN-LAST:event_RequisitionsActionPerformed

