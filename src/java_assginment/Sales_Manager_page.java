    private void Check_StockActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Check_StockActionPerformed
    File inventoryFile = new File(FILE_Item_Inventory);
    StringBuilder lowStockItems = new StringBuilder();
    boolean lowStockFound = false;

    try (BufferedReader br = new BufferedReader(new FileReader(inventoryFile))) {
        String line;
        while ((line = br.readLine()) != null) {
            try {
                // Assuming format: Date,Item,Quantity Sold,Price per Unit,Total Sales,Initial Stock,Quantity Remain
                String[] data = line.split(",");
                if (data.length != 7) {
                    throw new IllegalArgumentException("Invalid data format.");
                }

                String item = data[1].trim();
                int quantityRemain = Integer.parseInt(data[6].trim());

                // Check if the remaining quantity is below 20
                if (quantityRemain < 20) {
                    lowStockFound = true;
                    lowStockItems.append("Item: ").append(item)
                                 .append(" | Quantity Remain: ").append(quantityRemain)
                                 .append("\n");
                }
            } catch (Exception e) {
                // Log invalid lines (optional)
                System.err.println("Skipped invalid line: " + line);
            }
        }
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error reading the inventory file: " + e.getMessage());
        return;
    }

    // Show low stock items or notify if none are found
    if (lowStockFound) {
        JOptionPane.showMessageDialog(this, "Low Stock Items:\n\n" + lowStockItems.toString(), 
                                      "Low Stock Alert", JOptionPane.WARNING_MESSAGE);
    } else {
        JOptionPane.showMessageDialog(this, "All items have sufficient stock.", 
                                      "Stock Check", JOptionPane.INFORMATION_MESSAGE);
    }
    

    }//GEN-LAST:event_Check_StockActionPerformed

    private void Sales_ReportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Sales_ReportActionPerformed
     File itemsFile = new File(FILE_Sale_Report);
    if (itemsFile.exists()) {
        try (BufferedReader br = new BufferedReader(new FileReader(itemsFile))) {
            String line = br.readLine(); // Read the header
            if (line != null) {
                String[] headers = line.split(","); // Split the header for column names
                DefaultTableModel model = new DefaultTableModel(headers, 0); // Create a model with headers

                while ((line = br.readLine()) != null) {
                    String[] row = line.split(","); // Split each row by commas
                    model.addRow(row); // Add row to the model
                }

                jTable1.setModel(model); // Set the model to the JTable
            }
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error reading items data: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Items file not found.");
    }

    }//GEN-LAST:event_Sales_ReportActionPerformed

